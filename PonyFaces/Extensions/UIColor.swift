
import UIKit

extension UIColor {
    public class var pink: UIColor {
        return UIColor(red:0.94, green:0.37, blue:0.59, alpha:1.0)
    }
    
    public class var lightPink: UIColor {
        return UIColor(red:0.98, green:0.94, blue:1.00, alpha:1.0)
    }
    
    public class var lightPurple: UIColor {
        return UIColor(red:0.73, green:0.53, blue:0.89, alpha:1.0)
    }
}
